import { createHash } from 'node:crypto';
import { mkdir, readFile, stat, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { vocabChapters } from '../src/vocabData.js';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const audioDir = path.join(root, 'public', 'audio', 'examples');
const manifestPath = path.join(root, 'src', 'exampleAudioManifest.js');
const readLocalEnv = async () => {
  for (const fileName of ['.env.local', '.env']) {
    try {
      const content = await readFile(path.join(root, fileName), 'utf8');
      for (const line of content.split(/\r?\n/)) {
        const trimmed = line.trim();
        if (!trimmed || trimmed.startsWith('#') || !trimmed.includes('=')) continue;
        const [key, ...valueParts] = trimmed.split('=');
        if (process.env[key]) continue;
        process.env[key] = valueParts.join('=').replace(/^["']|["']$/g, '');
      }
    } catch {
      // Local env files are optional.
    }
  }
};

await readLocalEnv();

const apiKey = process.env.OPENAI_API_KEY;
const model = process.env.OPENAI_TTS_MODEL || 'gpt-4o-mini-tts';
const voice = process.env.OPENAI_TTS_VOICE || 'coral';

const args = new Set(process.argv.slice(2));
const getArg = (name, fallback = '') => {
  const prefix = `${name}=`;
  const match = process.argv.slice(2).find((arg) => arg.startsWith(prefix));
  return match ? match.slice(prefix.length) : fallback;
};

const limit = Number(getArg('--limit', '0'));
const force = args.has('--force');
const dryRun = args.has('--dry-run');

const audioIdFor = (text) => createHash('sha1').update(text).digest('hex').slice(0, 16);

const flattenEntries = (chapters) =>
  chapters.flatMap((chapter) =>
    chapter.sections.flatMap((section) =>
      section.entries.map((entry) => ({
        ...entry,
        chapterTitle: chapter.title,
        sectionTitle: section.title
      }))
    )
  );

const uniqueExamples = [...new Map(flattenEntries(vocabChapters).map((entry) => [entry.example, entry])).values()].filter(
  (entry) => entry.example && entry.example !== '例句'
);

const fileExists = async (filePath) => {
  try {
    await stat(filePath);
    return true;
  } catch {
    return false;
  }
};

const generateSpeech = async (text) => {
  const response = await fetch('https://api.openai.com/v1/audio/speech', {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${apiKey}`,
      'Content-Type': 'application/json'
    },
    body: JSON.stringify({
      model,
      voice,
      input: text,
      instructions: 'Read clearly and naturally for an English beginner shadowing practice. Use a calm teacher-like pace.',
      response_format: 'mp3'
    })
  });

  if (!response.ok) {
    const errorText = await response.text();
    throw new Error(`OpenAI TTS failed (${response.status}): ${errorText}`);
  }

  return Buffer.from(await response.arrayBuffer());
};

const writeManifest = async () => {
  const manifest = {};
  for (const entry of uniqueExamples) {
    const id = audioIdFor(entry.example);
    const fileName = `${id}.mp3`;
    if (await fileExists(path.join(audioDir, fileName))) {
      manifest[entry.example] = `/audio/examples/${fileName}`;
    }
  }

  const source = `// Generated by scripts/generate-example-audio.mjs.\n// Maps IELTS example sentences to pre-generated audio files.\nexport const exampleAudioManifest = ${JSON.stringify(
    manifest,
    null,
    2
  )};\n`;
  await writeFile(manifestPath, source);
  return Object.keys(manifest).length;
};

const main = async () => {
  await mkdir(audioDir, { recursive: true });

  const targets = limit > 0 ? uniqueExamples.slice(0, limit) : uniqueExamples;
  let generated = 0;
  let skipped = 0;

  if (!apiKey && !dryRun) {
    console.log('OPENAI_API_KEY is not set. Existing audio manifest will be refreshed, but no new audio will be generated.');
  }

  for (const entry of targets) {
    const id = audioIdFor(entry.example);
    const filePath = path.join(audioDir, `${id}.mp3`);
    const exists = await fileExists(filePath);

    if (exists && !force) {
      skipped += 1;
      continue;
    }

    if (dryRun || !apiKey) {
      console.log(`[dry] ${entry.chapterTitle} / ${entry.sectionTitle} / ${entry.term}: ${entry.example}`);
      continue;
    }

    console.log(`[tts] ${generated + 1}/${targets.length} ${entry.term}: ${entry.example}`);
    const audio = await generateSpeech(entry.example);
    await writeFile(filePath, audio);
    generated += 1;
  }

  const manifestCount = await writeManifest();
  console.log(`Done. generated=${generated}, skipped=${skipped}, manifest=${manifestCount}, totalExamples=${uniqueExamples.length}`);
};

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
