import { createHash } from 'node:crypto';
import { mkdir, readFile, rm, stat, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { normalizeAudioFile } from './audio-utils.mjs';
import { listeningVocabEntries } from '../src/listeningVocabData.js';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const wordAudioDir = path.join(root, 'public', 'audio', 'listening', 'words');
const exampleAudioDir = path.join(root, 'public', 'audio', 'listening', 'examples');
const manifestPath = path.join(root, 'src', 'listeningAudioManifest.js');

const readLocalEnv = async () => {
  for (const fileName of ['.env.local', '.env']) {
    try {
      const content = await readFile(path.join(root, fileName), 'utf8');
      for (const line of content.split(/\r?\n/)) {
        const trimmed = line.trim();
        if (!trimmed || trimmed.startsWith('#') || !trimmed.includes('=')) continue;
        const [key, ...valueParts] = trimmed.split('=');
        if (process.env[key]) continue;
        process.env[key] = valueParts.join('=').replace(/^["']|["']$/g, '');
      }
    } catch {
      // Local env files are optional.
    }
  }
};

await readLocalEnv();

const apiKey = process.env.OPENAI_API_KEY;
const model = process.env.OPENAI_TTS_MODEL || 'gpt-4o-mini-tts';
const voice = process.env.OPENAI_TTS_VOICE || 'coral';
const args = new Set(process.argv.slice(2));

const getArg = (name, fallback = '') => {
  const prefix = `${name}=`;
  const match = process.argv.slice(2).find((arg) => arg.startsWith(prefix));
  return match ? match.slice(prefix.length) : fallback;
};

const limit = Number(getArg('--limit', '0'));
const force = args.has('--force');
const dryRun = args.has('--dry-run');
const only = getArg('--only', 'all');

const audioIdFor = (kind, text) => createHash('sha1').update(`${kind}:${text}`).digest('hex').slice(0, 16);

const fileExists = async (filePath) => {
  try {
    await stat(filePath);
    return true;
  } catch {
    return false;
  }
};

const generateSpeech = async (text, kind) => {
  const instructions =
    kind === 'word'
      ? 'Say only this IELTS listening vocabulary word or phrase. Speak clearly, naturally, and slowly enough for an English learner to distinguish similar-sounding words.'
      : 'Read this IELTS listening example sentence clearly and naturally. Use a calm teacher-like pace, with careful articulation for an English learner.';

  const response = await fetch('https://api.openai.com/v1/audio/speech', {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${apiKey}`,
      'Content-Type': 'application/json'
    },
    body: JSON.stringify({
      model,
      voice,
      input: text,
      instructions,
      response_format: 'mp3'
    })
  });

  if (!response.ok) {
    const errorText = await response.text();
    throw new Error(`OpenAI TTS failed (${response.status}): ${errorText}`);
  }

  return Buffer.from(await response.arrayBuffer());
};

const buildTargets = () => {
  const entries = limit > 0 ? listeningVocabEntries.slice(0, limit) : listeningVocabEntries;
  const targets = [];
  for (const entry of entries) {
    if (only === 'all' || only === 'words') {
      targets.push({
        kind: 'word',
        entry,
        text: entry.term,
        spokenText: `${entry.term}.`,
        dir: wordAudioDir,
        urlBase: '/audio/listening/words'
      });
    }
    if ((only === 'all' || only === 'examples') && entry.example) {
      targets.push({
        kind: 'example',
        entry,
        text: entry.example,
        spokenText: entry.example,
        dir: exampleAudioDir,
        urlBase: '/audio/listening/examples'
      });
    }
  }
  return targets;
};

const writeManifest = async () => {
  const words = {};
  const examples = {};
  for (const entry of listeningVocabEntries) {
    const wordId = audioIdFor('word', entry.term);
    const wordFileName = `${wordId}.mp3`;
    if (await fileExists(path.join(wordAudioDir, wordFileName))) {
      words[entry.id] = `/audio/listening/words/${wordFileName}`;
    }

    if (entry.example) {
      const exampleId = audioIdFor('example', entry.example);
      const exampleFileName = `${exampleId}.mp3`;
      if (await fileExists(path.join(exampleAudioDir, exampleFileName))) {
        examples[entry.id] = `/audio/listening/examples/${exampleFileName}`;
      }
    }
  }

  const source = `// Generated by scripts/generate-listening-audio.mjs.\n// Maps listening vocabulary ids to pre-generated word and example audio files.\nexport const listeningAudioManifest = ${JSON.stringify(
    { words, examples },
    null,
    2
  )};\n`;
  await writeFile(manifestPath, source);
  return { words: Object.keys(words).length, examples: Object.keys(examples).length };
};

const main = async () => {
  await mkdir(wordAudioDir, { recursive: true });
  await mkdir(exampleAudioDir, { recursive: true });

  const targets = buildTargets();
  let generated = 0;
  let skipped = 0;

  if (!apiKey && !dryRun) {
    console.log('OPENAI_API_KEY is not set. Existing listening audio manifest will be refreshed, but no new audio will be generated.');
  }

  for (let index = 0; index < targets.length; index += 1) {
    const target = targets[index];
    const id = audioIdFor(target.kind, target.text);
    const filePath = path.join(target.dir, `${id}.mp3`);
    const exists = await fileExists(filePath);

    if (exists && !force) {
      skipped += 1;
      continue;
    }

    if (dryRun || !apiKey) {
      console.log(`[dry] ${target.kind} ${target.entry.number}/${listeningVocabEntries.length} ${target.entry.term}: ${target.text}`);
      continue;
    }

    console.log(`[tts] ${index + 1}/${targets.length} ${target.kind} ${target.entry.term}: ${target.text}`);
    const audio = await generateSpeech(target.spokenText, target.kind);
    await writeFile(filePath, audio);
    try {
      await normalizeAudioFile(filePath);
    } catch (error) {
      await rm(filePath, { force: true });
      console.warn(`[warn] removed unusable audio for ${target.kind} ${target.entry.term}: ${error.message}`);
    }
    generated += 1;
  }

  const manifestCount = await writeManifest();
  console.log(
    `Done. generated=${generated}, skipped=${skipped}, manifestWords=${manifestCount.words}, manifestExamples=${manifestCount.examples}, totalEntries=${listeningVocabEntries.length}`
  );
};

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
