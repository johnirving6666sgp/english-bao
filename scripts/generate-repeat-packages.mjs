import { createHash } from 'node:crypto';
import { mkdtemp, mkdir, readFile, rm, stat, writeFile } from 'node:fs/promises';
import { execFile } from 'node:child_process';
import { promisify } from 'node:util';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { exampleAudioManifest } from '../src/exampleAudioManifest.js';
import { vocabChapters } from '../src/vocabData.js';

const execFileAsync = promisify(execFile);
const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const packageDir = path.join(root, 'public', 'audio', 'repeat-packages');
const manifestPath = path.join(root, 'src', 'repeatPackageManifest.js');
const silenceBetweenRounds = 0.55;
const silenceBetweenEntries = 0.9;

const getArg = (name, fallback = '') => {
  const prefix = `${name}=`;
  const match = process.argv.slice(2).find((arg) => arg.startsWith(prefix));
  return match ? match.slice(prefix.length) : fallback;
};

const args = new Set(process.argv.slice(2));
const limit = Number(getArg('--limit', '0'));
const force = args.has('--force');

const hashId = (text) => createHash('sha1').update(text).digest('hex').slice(0, 16);
const escapeConcatPath = (filePath) => filePath.replaceAll("'", "'\\''");

const fileExists = async (filePath) => {
  try {
    await stat(filePath);
    return true;
  } catch {
    return false;
  }
};

const audioDuration = async (filePath) => {
  const { stdout } = await execFileAsync('ffprobe', [
    '-v',
    'error',
    '-show_entries',
    'format=duration',
    '-of',
    'default=noprint_wrappers=1:nokey=1',
    filePath
  ]);
  return Number(stdout.trim()) || 0;
};

const createSilence = async (filePath, duration) => {
  await execFileAsync('ffmpeg', [
    '-y',
    '-hide_banner',
    '-loglevel',
    'error',
    '-f',
    'lavfi',
    '-i',
    'anullsrc=channel_layout=mono:sample_rate=24000',
    '-t',
    String(duration),
    '-codec:a',
    'libmp3lame',
    '-b:a',
    '128k',
    filePath
  ]);
};

const sectionKey = (chapterId, sectionTitle) => `${chapterId}::${sectionTitle}`;

const buildSectionPackage = async ({ chapter, section, tempDir }) => {
  const key = sectionKey(chapter.id, section.title);
  const fileName = `${hashId(key)}.mp3`;
  const outputPath = path.join(packageDir, fileName);

  if ((await fileExists(outputPath)) && !force) {
    return { skipped: true, key, fileName };
  }

  const roundSilence = path.join(tempDir, 'round-silence.mp3');
  const entrySilence = path.join(tempDir, 'entry-silence.mp3');
  if (!(await fileExists(roundSilence))) await createSilence(roundSilence, silenceBetweenRounds);
  if (!(await fileExists(entrySilence))) await createSilence(entrySilence, silenceBetweenEntries);

  const lines = [];
  const offsets = {};
  let cursor = 0;

  for (const [index, entry] of section.entries.entries()) {
    const audioUrl = exampleAudioManifest[entry.example];
    if (!audioUrl) continue;
    const sourcePath = path.join(root, 'public', audioUrl.replace(/^\//, ''));
    if (!(await fileExists(sourcePath))) continue;

    const id = `${chapter.id}-${section.title}-${index}-${entry.term}`;
    const duration = await audioDuration(sourcePath);
    offsets[id] = Number(cursor.toFixed(3));

    lines.push(`file '${escapeConcatPath(sourcePath)}'`);
    cursor += duration;
    lines.push(`file '${escapeConcatPath(roundSilence)}'`);
    cursor += silenceBetweenRounds;
    lines.push(`file '${escapeConcatPath(sourcePath)}'`);
    cursor += duration;
    lines.push(`file '${escapeConcatPath(entrySilence)}'`);
    cursor += silenceBetweenEntries;
  }

  if (!lines.length) return null;

  const listPath = path.join(tempDir, `${fileName}.txt`);
  await writeFile(listPath, `${lines.join('\n')}\n`);
  await execFileAsync('ffmpeg', [
    '-y',
    '-hide_banner',
    '-loglevel',
    'error',
    '-f',
    'concat',
    '-safe',
    '0',
    '-i',
    listPath,
    '-codec:a',
    'libmp3lame',
    '-b:a',
    '128k',
    outputPath
  ]);

  return {
    skipped: false,
    key,
    fileName,
    duration: Number(cursor.toFixed(3)),
    offsets
  };
};

const writeManifest = async (items) => {
  const existing = await readExistingManifest();
  const manifest = { ...existing };

  for (const item of items) {
    if (!item) continue;
    const previous = manifest[item.key] || {};
    manifest[item.key] = {
      ...previous,
      url: `/audio/repeat-packages/${item.fileName}`,
      duration: item.duration ?? previous.duration ?? 0,
      offsets: item.offsets ?? previous.offsets ?? {}
    };
  }

  const source = `// Generated by scripts/generate-repeat-packages.mjs.\n// Maps IELTS vocabulary sections to long continuous example audio packages.\nexport const repeatPackageManifest = ${JSON.stringify(
    manifest,
    null,
    2
  )};\n`;
  await writeFile(manifestPath, source);
  return Object.keys(manifest).length;
};

const readExistingManifest = async () => {
  try {
    const source = await readFile(manifestPath, 'utf8');
    const json = source.match(/export const repeatPackageManifest = ([\s\S]*);\n?$/)?.[1];
    return json ? JSON.parse(json) : {};
  } catch {
    return {};
  }
};

const main = async () => {
  await mkdir(packageDir, { recursive: true });
  const tempDir = await mkdtemp(path.join(os.tmpdir(), 'english-bao-repeat-packages-'));
  const sections = vocabChapters.flatMap((chapter) =>
    chapter.sections.map((section) => ({ chapter, section }))
  );
  const targets = limit > 0 ? sections.slice(0, limit) : sections;
  const results = [];

  try {
    for (const [index, target] of targets.entries()) {
      const label = `${target.chapter.title} / ${target.section.title}`;
      console.log(`[package] ${index + 1}/${targets.length} ${label}`);
      results.push(await buildSectionPackage({ ...target, tempDir }));
    }
  } finally {
    await rm(tempDir, { recursive: true, force: true });
  }

  const manifestCount = await writeManifest(results);
  const generated = results.filter((item) => item && !item.skipped).length;
  const skipped = results.filter((item) => item?.skipped).length;
  console.log(`Done. generated=${generated}, skipped=${skipped}, manifest=${manifestCount}`);
};

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
